const filter_fuel_type = document.getElementById("filter_fuel_type");
const filter_material_type = document.getElementById("filter_material_type");
const filter_placing = document.getElementById("filter_placing");

filter_fuel_type.addEventListener("change", (e) => {
  filtrado();
});
filter_material_type.addEventListener("change", (e) => {
  filtrado();
});
filter_placing.addEventListener("change", (e) => {
  filtrado();
});

function filtrado() {
  let filtradoDeProductos = [...productsList];
  const fuel_type = filter_fuel_type.value || "";
  const material_type = filter_material_type.value || "";
  const placing = filter_placing.value || "";
  productosDestacados.innerHTML = "";

  let filtros = {};

  if (fuel_type === "fuel_type" || fuel_type === "All") {
    filtrado_productos;
  } else {
    filtros["fuel_type"] = fuel_type;
  }

  if (fuel_type === "MATERIAL" || material_type === "All") {
    filtrado_productos;
  } else {
    filtros["material"] = material;
  }

  if (placing === "PLACING" || placing === "All") {
    filtrado_productos;
  } else {
    filtros["placing"] = placing;
  }

  const filtracion = filtradoDeProductos.filter((p) => {
    return (
      (filtros["fuel_type"] ? p.tipo === filtros["fuel_type"] : true) &&
      (filtros["material"] ? p.material === filtros["material"] : true) &&
      (filtros["placing"] ? p.tipo2 === filtros["placing"] : true)
    );
  });

  mostrar(filtracion);
  mostrar();
}

const productosDestacados = document.getElementById("productos_destacados");
let filtrado_productos = productsList;
mostrar(productsList);

function mostrar(productsList) {
  productsList.forEach((product) => {
    productosDestacados.innerHTML += `<a href= './item.html?id=${product.num}' class="card_fil">
        <figure class= "card_figure"><img class= "card_img" src="${product.imageProduct}"></figure>
        <article class="card_article">
            <h2>${product.item}</h2>
            <p>${product.tipo}</p>
            <h3 class="texto_precio">${product.price}</h3>
            <h5> ${product.material}</h5>
        </article>
    </a>`;
  });
}
